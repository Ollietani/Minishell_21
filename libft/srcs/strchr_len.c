#include "libft.h"

int	ft_strchr_len(const char *s, int c)
{
	char	d;
	int		i;

	d = c - '0';
	i = 0;
	while (*s)
	{
		if (*s == c)
			return (i);
		s++;
		i++;
	}
	return (-1);
}
